/**
 * Scans extension/assets/kanye/ and writes extension/sidepanel/kanye-images.js.
 * Run after adding or removing images: npm run assets:manifest
 */
const fs = require('fs');
const path = require('path');

const ASSETS_DIR = path.join(__dirname, '../extension/assets/kanye');
const OUT = path.join(__dirname, '../extension/sidepanel/kanye-images.js');
const EXT = new Set(['.jpg', '.jpeg', '.png', '.gif', '.webp', '.avif']);

const files = fs.existsSync(ASSETS_DIR)
  ? fs
      .readdirSync(ASSETS_DIR)
      .filter((f) => EXT.has(path.extname(f).toLowerCase()))
      .sort()
  : [];

const body = `/**
 * AUTO-GENERATED by npm run assets:manifest — do not edit by hand.
 * Source: extension/assets/kanye/
 */
(function (root) {
  root.FAA_KANYE_IMAGES = ${JSON.stringify(files, null, 2)};
})(typeof window !== 'undefined' ? window : globalThis);
`;

fs.writeFileSync(OUT, body);
console.log(`Wrote ${files.length} image(s) to ${path.relative(process.cwd(), OUT)}`);
